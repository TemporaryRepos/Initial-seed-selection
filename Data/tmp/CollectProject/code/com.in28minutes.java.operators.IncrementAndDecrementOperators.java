class IncrementAndDecrementOperators {
	public static void main(java.lang.String[] args) {
		int i = 25;
		int j = ++i;
		java.lang.System.out.println((i + " ") + j);
		i = 25;
		j = i++;
		java.lang.System.out.println((i + " ") + j);
		i = 25;
		j = --i;
		java.lang.System.out.println((i + " ") + j);
		i = 25;
		j = i--;
		java.lang.System.out.println((i + " ") + j);
		final int k = 100;
	}
}