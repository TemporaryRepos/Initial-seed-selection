public class IINC {
	public static void main(java.lang.String[] args) {
		int a = -127;
		a++;
		java.lang.System.out.println(a);
		int b = 129;
		b++;
		java.lang.System.out.println(b);
		int c = 321;
		c++;
		java.lang.System.out.println(c);
		int d = 0;
		d++;
		java.lang.System.out.println(d);
	}
}