public class Constants {
	public static void main(java.lang.String[] args) throws java.lang.Exception {
		int i = 0;
		switch (i) {
			case ((int) (java.lang.Float.NaN)) :
				java.lang.System.out.println("Float.NaN is a constant!");
				break;
			case ((int) (java.lang.Float.MIN_VALUE)) + 1 :
				java.lang.System.out.println("Float.MIN_VALUE is a constant!");
				break;
			case ((int) (java.lang.Float.MIN_NORMAL)) + 2 :
				java.lang.System.out.println("Float.MIN_NORMAL is a constant!");
				break;
			case java.lang.Float.MIN_EXPONENT :
				java.lang.System.out.println("Float.MIN_EXPONENT is a constant!");
				break;
			case java.lang.Float.MAX_EXPONENT :
				java.lang.System.out.println("Float.MAX_EXPONENT is a constant!");
				break;
			case ((int) (java.lang.Float.MAX_VALUE)) - 1 :
				java.lang.System.out.println("Float.MAX_VALUE is a constant!");
				break;
			case ((int) (java.lang.Float.POSITIVE_INFINITY)) :
				java.lang.System.out.println("Float.POSITIVE_INFINITY is a constant!");
				break;
			case ((int) (java.lang.Float.NEGATIVE_INFINITY)) :
				java.lang.System.out.println("Float.NEGATIVE_INFINITY is a constant!");
				break;
		}
	}
}