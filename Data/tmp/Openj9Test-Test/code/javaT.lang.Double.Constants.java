public class Constants {
	public static void main(java.lang.String[] args) throws java.lang.Exception {
		int i = 0;
		switch (i) {
			case ((int) (java.lang.Double.NaN)) :
				java.lang.System.out.println("Double.NaN is a constant!");
				break;
			case ((int) (java.lang.Double.MIN_VALUE)) + 1 :
				java.lang.System.out.println("Double.MIN_VALUE is a constant!");
				break;
			case ((int) (java.lang.Double.MIN_NORMAL)) + 2 :
				java.lang.System.out.println("Double.MIN_NORMAL is a constant!");
				break;
			case java.lang.Double.MIN_EXPONENT :
				java.lang.System.out.println("Double.MIN_EXPONENT is a constant!");
				break;
			case java.lang.Double.MAX_EXPONENT :
				java.lang.System.out.println("Double.MAX_EXPONENT is a constant!");
				break;
			case ((int) (java.lang.Double.MAX_VALUE)) - 1 :
				java.lang.System.out.println("Double.MAX_VALUE is a constant!");
				break;
			case ((int) (java.lang.Double.POSITIVE_INFINITY)) :
				java.lang.System.out.println("Double.POSITIVE_INFINITY is a constant!");
				break;
			case ((int) (java.lang.Double.NEGATIVE_INFINITY)) :
				java.lang.System.out.println("Double.NEGATIVE_INFINITY is a constant!");
				break;
		}
	}
}